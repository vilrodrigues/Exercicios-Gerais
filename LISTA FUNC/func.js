function parOuImpar(num){
    if (num % 2 == 0){
        alert("Par");
    }
    else {
        alert("Impar");
    }
    return;
}

function totalPares(num){
    var somador = 0;
    if (num % 2 == 0){
        somador += 1;
    }
}
function totalImpares(num){
    var  somador = 0;
    if (num % 2 != 0){
        somador += 1;
    }
}